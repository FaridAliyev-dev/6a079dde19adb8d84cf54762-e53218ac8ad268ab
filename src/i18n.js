export function initLanguageSwitcher() {
  const options = document.querySelectorAll('.lang-option');

  if (options.length === 0) return;

  // Set language
  const setLanguage = (lang) => {
    localStorage.setItem('preferredLang', lang);
    
    // Update active state in UI
    options.forEach(opt => {
      if (opt.getAttribute('data-lang') === lang) {
        opt.classList.add('active');
      } else {
        opt.classList.remove('active');
      }
    });

    // Apply translations
    const dict = translations[lang];
    if (dict) {
      for (const [selector, key] of Object.entries(domMapping)) {
        const el = document.querySelector(selector);
        if (el && dict[key]) {
          el.innerHTML = dict[key];
        }
      }
    }

    // Update documentation sidebar indicator if it exists
    const activeDocLink = document.querySelector('.sidebar-nav a.active');
    if (activeDocLink && typeof window.updateDocIndicator === 'function') {
      window.updateDocIndicator(activeDocLink);
    }
  };

  // Handle option click
  options.forEach(opt => {
    opt.addEventListener('click', (e) => {
      const selectedLang = e.target.getAttribute('data-lang');
      setLanguage(selectedLang);
    });
  });

  // Load preferred language or default to English
  const savedLang = localStorage.getItem('preferredLang') || 'en';
  setLanguage(savedLang);
}
